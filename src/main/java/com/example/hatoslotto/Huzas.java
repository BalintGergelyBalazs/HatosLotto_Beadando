package com.example.hatoslotto;

public class Huzas {
    private int id;
    private int ev;
    private int het;

    public Huzas(int id, int ev, int het) {
        this.id = id;
        this.ev = ev;
        this.het = het;
    }

    public int getId() {
        return id;
    }

    public int getEv() {
        return ev;
    }

    public int getHet() {
        return het;
    }
}
